let amount = document.querySelector("input");
let show = document.querySelector(".show");


for(part of selecte){
  for(code in countryList){
    let newOption = document.createElement("option");
    newOption.innerText=code;
    newOption.value=code;
    if( code === "USD" && part.name === "from"  )
    {
     newOption.selected = "selected";
    } else if( code === "PKR" && part.name === "to"){
      newOption.selected = "selected";
    }
    part.append(newOption);
  }
}
let changeFlag1 = (tag) => {
  let newCode = tag.value;
  let counCode = countryList[newCode];
  let newUrl = `https://flagsapi.com/${counCode}/flat/64.png`
  img1.src = newUrl;
}
let changeFlag2 = (tag) => {
  let newCode = tag.value;
  let counCode = countryList[newCode];
  let newUrl = `https://flagsapi.com/${counCode}/flat/64.png`
  img2.src = newUrl;
}
